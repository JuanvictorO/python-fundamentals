movieName = "Avengers: Endgame"

# string[start:end] - incice começa em 0 e vai até n-1
print(movieName[0:8]) # Avengers

# Buscar toda string até a última posição
print(movieName[:17]) # Avengers: Endgame

# Buscar toda string da terceira até a última posição
print(movieName[3:]) # ngers: Endgame

# string[start:end:step]
# indice começa em 0 e vai até n-1
# step é a quantidade de caracteres que será pulada
print(movieName[::2]) # Aegr

# toda string ao contrário
print(movieName[::-1]) # emagnEdnega: sregnevA

# Buscar toda string ímpar
print(movieName[1::2]) # vegr nme